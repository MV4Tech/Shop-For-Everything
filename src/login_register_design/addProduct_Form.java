        public boolean verifyFields(){
            
        String head = jTextField_head.getText();
        String price = String.valueOf(PriceField.getText().trim());
        String phoneNumber = String.valueOf(jTextField_phoneNumber.getText().trim());
        String location = jTextField_location.getText();
        String description = jTextArea_description.getText();
       
                    if(head.trim().equals("") || price.equals("") || phoneNumber.trim().equals("") ||
                location.trim().equals("") || description.trim().equals("") || price.equals("^[-a-zA-Z0-9._]+")){
            
               JOptionPane.showMessageDialog(null, "One or More Fields Are Empty", "Empty Fields",2);
               return false;
        }else if(head.length() > 20){
             JOptionPane.showMessageDialog(null, "Title field must be under 20 characters", "Empty Fields",2);
             return false;
        }else if(location.length() > 30){
             JOptionPane.showMessageDialog(null, "Location filed must be under 30 characters", "Empty Fields",2);
             return false;
        }else if(phoneNumber.length() >= 10){
             JOptionPane.showMessageDialog(null, "Phone field must be under or 10  characters", "Empty Fields",2);
             return false;
        }
        else{
            return true;
        }
        
                    
    }
    
    
