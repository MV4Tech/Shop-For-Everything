    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        // Get information
        
        
        if(verifyFields()){
            try {
                String head = jTextField_head.getText();
                Double price = Double.parseDouble(PriceField.getText().trim());
                String phoneNumber = String.valueOf(jTextField_phoneNumber.getText().trim());
                String location = jTextField_location.getText();
                String description = jTextArea_description.getText();
                //Date date = java.sql.Date.valueOf(java.time.LocalDate.now());
                java.sql.Timestamp date = new java.sql.Timestamp(new java.util.Date().getTime());
         
               
                
                
                PreparedStatement ps;
                ResultSet rs;
                
                
                // create statement
                String addProductQuery = "INSERT INTO `products`(`head`, `description`, `location`, `phone_number`, `user_id`, `price`, `date`)"
                        + " VALUES(?,?,?,?,?,?,?)";
                
                ps = My_CNX.getConnection().prepareStatement(addProductQuery);
                
                ps.setString(1,head);
                ps.setString(2,description);
                ps.setString(3,location);
                ps.setString(4,phoneNumber);
                ps.setInt(5,Login_Form.currentUserId);
                ps.setDouble(6,price);
                ps.setTimestamp(7, date);
                
                
                if(ps.executeUpdate() != 0){
                        
                    JOptionPane.showMessageDialog(null, "Your Item Has Been Added Successfully!");
                        Menu_Form form = new Menu_Form();
                        form.setVisible(true);
                        form.pack();
                        form.setLocationRelativeTo(null);
                        this.dispose();
                } else{
                     JOptionPane.showMessageDialog(null, "Error: Check Your Information!");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(addProduct_Form.class.getName()).log(Level.SEVERE, null, ex);
            } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Price Field Should Be With Numerals", "Empty Fields",2);
            }
               
            
            
            
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

